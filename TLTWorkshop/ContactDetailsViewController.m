- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"ContactDetails";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier];
    }
    
    // Configure the cell...
    NSInteger row = [indexPath row];
    switch (row) {
        case 0:
            cell.textLabel.text = @"Name";
            cell.detailTextLabel.text = [NSString stringWithFormat:@"%@ %@", self.person.firstName, self.person.lastName];
            break;
            
        case 1:
            cell.textLabel.text = @"Address";
            cell.detailTextLabel.text = self.person.address;
            break;
            
        case 2:
            cell.textLabel.text = @"Phone";
            cell.detailTextLabel.text = self.person.phone;
            break;
            
        case 3:
            cell.textLabel.text = @"Mobile";
            cell.detailTextLabel.text = self.person.mobile;
            break;
            
        case 4:
            cell.textLabel.text = @"email";
            cell.detailTextLabel.text = self.person.email;
            break;
            
        default:
            break;
    }
    
    return cell;
}

